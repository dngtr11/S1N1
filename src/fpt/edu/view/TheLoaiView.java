    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        // TODO add your handling code here:
        if (service.getAll().size() > 0) {
            if (txtSearch.getText().isBlank()) {
                list = service.search(txtSearch.getText());
                showData(list);
            }
            if (jComboBox1.getSelectedIndex() == 0) {
                list = service.search("còn");
                showData(list);
            } else {
                list = service.search("hết");
                showData(list);
            }
        }
    }//GEN-LAST:event_btSearchActionPerformed

    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        // TODO add your handling code here:
        if (tbTheLoai.getSelectedRow()!=-1) {
            String ma = tbTheLoai.getValueAt(tbTheLoai.getSelectedRow(), 0).toString();
            if (service.remove(ma)) {
                JOptionPane.showMessageDialog(this, "xóa thành công");
                list = service.getAll();
                showData(list);
            }else{
                JOptionPane.showMessageDialog(this, "xóa thất bại");
            }
        }
    }//GEN-LAST:event_btRemoveActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        // TODO add your handling code here:
        if (tbTheLoai.getSelectedRow()>=0) {
            TheLoai tl = service.getAll().get(tbTheLoai.getSelectedRow());
            JOptionPane.showMessageDialog(rootPane, service.update(tl.getMaTL(), showDetai()));
            list = service.getAll();
            showData(list);
        }
    }//GEN-LAST:event_btUpdateActionPerformed

    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        if (service.add(showDetai())) {
            JOptionPane.showMessageDialog(this, "them thành công");
            list= service.getAll();
            showData(list);
        }else{
            JOptionPane.showMessageDialog(this, "them thât bại");
        }
    }//GEN-LAST:event_btAddActionPerformed

