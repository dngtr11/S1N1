    void showData(List<TheLoai> list){
        dtm.setRowCount(0);
        for (TheLoai tl : list) {
            dtm.addRow(new Object[]{
                tl.getMaTL(),
                tl.getTenTL(),
                tl.isTrangThai() == true ? "còn" : "hết"
            });
        }
    }
    public  TheLoai showDetai(){
        TheLoai tl = new TheLoai();
        tl.setMaTL(txtMa.getText());
        tl.setTenTL(txtLoai.getText());
        tl.setTrangThai(rdHoatDong.isSelected());
        return tl;
    }

